// Timer interrupt in order to provide a sample frequency of 48 kHz
void InitTimerInt(){

	SysCtlPeripheralEnable(SYSCTL_PERIPH_TIMER0);

	TimerConfigure(TIMER0_BASE, TIMER_CFG_PERIODIC);

	ui32Period = ui32SysClkFreq / SAMPLE_FREQ;

	TimerLoadSet(TIMER0_BASE, TIMER_A, ui32Period - 1);

	IntEnable(INT_TIMER0A);
	TimerIntEnable(TIMER0_BASE, TIMER_TIMA_TIMEOUT);
	IntMasterEnable();
}

// Handler code for the interruption call of PortJ
// When called the program changes de effect by changing the ui8Count number
void PortJIntHandler(void) {

	uint16_t i;

	// Button bounce delay
	SysCtlDelay(ui32SysClkFreq/10);

	GPIOIntClear(GPIO_PORTJ_BASE, GPIO_INT_PIN_0);
	TimerDisable(TIMER0_BASE, TIMER_A);

	ui8Count++;
	if (ui8Count > 4)
		ui8Count = 0;

	ui16Index = 0;

	// Reset all LEDs
	GPIOPinWrite(GPIO_PORTN_BASE, GPIO_PIN_0 | GPIO_PIN_1, 0);
	GPIOPinWrite(GPIO_PORTF_BASE, GPIO_PIN_0 | GPIO_PIN_4, 0);

	// Choose the LEDs which corresponds to the effect selected
	switch (ui8Count){

	case 1:
		// DISTORTION
		// D3 on
		GPIOPinWrite(GPIO_PORTF_BASE, GPIO_PIN_4, GPIO_PIN_4);

		break;

	case 2:
		// DELAY  /  ECHO
		// D2 on

		for(i=0 ; i < SAMPLE_FREQ ; i++){
			soundWave[i]=0;
		}

		ui16Index = 0;
		ui16Delay = 0;

		GPIOPinWrite(GPIO_PORTN_BASE, GPIO_PIN_0, GPIO_PIN_0);

		break;

	case 3:
		// LOOP
		// D2 and D3 on
		GPIOPinWrite(GPIO_PORTN_BASE, GPIO_PIN_0, GPIO_PIN_0);
		GPIOPinWrite(GPIO_PORTF_BASE, GPIO_PIN_4, GPIO_PIN_4);

		// Show that it's recording
		// Light LED D4
		GPIOPinWrite(GPIO_PORTF_BASE, GPIO_PIN_0, GPIO_PIN_0);
		recording = true;

		break;

	case 4:
		//TREMOLO
		// D1 and D3 on
		GPIOPinWrite(GPIO_PORTN_BASE, GPIO_PIN_1, GPIO_PIN_1);

		break;

	default:
		// No Effect
		// Both LED off

		GPIOPinWrite(GPIO_PORTN_BASE, GPIO_PIN_0 | GPIO_PIN_1,
				GPIO_PIN_0 | GPIO_PIN_1);
		GPIOPinWrite(GPIO_PORTF_BASE, GPIO_PIN_0 | GPIO_PIN_4,
				GPIO_PIN_0 | GPIO_PIN_4);
	}


	TimerLoadSet(TIMER0_BASE, TIMER_A, ui32Period - 1);
	TimerEnable(TIMER0_BASE, TIMER_A);
}

void Timer0IntHandler(void) {
// Clear the timer interrupt
	TimerIntClear(TIMER0_BASE, TIMER_TIMA_TIMEOUT);

	Effects();
}

// Initialization of PORTJ PIN0, the button for the above interruption
void InitButton(void) {

	SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOJ);

	while (!SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOJ)) {
	}

	GPIOPinTypeGPIOInput(GPIO_PORTJ_BASE, GPIO_PIN_0);

	GPIOPadConfigSet(GPIO_PORTJ_BASE, GPIO_PIN_0, GPIO_STRENGTH_2MA,
	GPIO_PIN_TYPE_STD_WPU);
	GPIOIntTypeSet(GPIO_PORTJ_BASE, GPIO_PIN_0, GPIO_FALLING_EDGE);
	GPIOIntRegister(GPIO_PORTJ_BASE, PortJIntHandler);
	GPIOIntEnable(GPIO_PORTJ_BASE, GPIO_INT_PIN_0);

}

