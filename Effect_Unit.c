void Effects(void){

	switch (ui8Count) {

	case distortion:
		//First Effect
		//DISTORTION

		ReadADC();

		if (convertedValue > MAX_DISTORTION )
			convertedValue = MAX_DISTORTION;
		else if (convertedValue < - MAX_DISTORTION)
			convertedValue = - MAX_DISTORTION;

		WriteDAC(convertedValue + 32768);

		break;

	case delay:
		//Second Effect
		//DELAY

		ReadADC();

		WriteDAC(soundWave[ui16Index] + 32768);
		soundWave[ui16Index] = convertedValue;

		ui16Index++;
		ui16Index = ui16Index % SAMPLE_FREQ;

		break;

	case loop:
		//Third Effect
		//LOOP

		// If recording then...
		if (recording){

			ReadADC();
			soundWave[ui16Index] = convertedValue;

			ui16Index++;

			if (ui16Index == SAMPLE_FREQ) {
				recording = false;
				ui16Index = 0;
				GPIOPinWrite(GPIO_PORTF_BASE, GPIO_PIN_0, 0);
			}
		}

		// If not recording then...
		if (!recording) {

			WriteDAC(soundWave[ui16Index] + 32768);

			ui16Index++;
			ui16Index = ui16Index % SAMPLE_FREQ;
		}

		break;

	case tremolo:
		//Fourth Effect
		//TREMOLO

		ReadADC();
		WriteDAC( (uint16_t) (convertedValue*tremolo_wave[ui16TmloCount] + 32768) );

		ui16TmloCount++;
		ui16TmloCount = ui16TmloCount % 10000;

		break;

	default:

		ReadADC();
		WriteDAC(convertedValue + 32768);

	}
}
