// Initializing the DAC
void InitDAC(void) {

	// Commands to initialing the DAC
	uint8_t command[6] = { 0x01, 0x08, 0x18, 0x28, 0x08 };
	uint8_t first_data[6] = { 0x00, 0x40, 0x00, 0x00, 0xFF };
	uint8_t second_data[6] = { 0x00, 0x00, 0x01, 0xFE, 0xFF };
	uint8_t uintIndex;

	for (uintIndex = 0; uintIndex < 2; uintIndex++) {

		GPIOPinWrite(GPIO_PORTD_BASE, GPIO_PIN_2, 0);

		SSIDataPut(SSI3_BASE, command[uintIndex]);
		while (SSIBusy(SSI3_BASE)) {
		}

		SSIDataPut(SSI3_BASE, first_data[uintIndex]);
		while (SSIBusy(SSI3_BASE)) {
		}
		SSIDataPut(SSI3_BASE, second_data[uintIndex]);
		while (SSIBusy(SSI3_BASE)) {
		}

		GPIOPinWrite(GPIO_PORTD_BASE, GPIO_PIN_2, GPIO_PIN_2);
	}
}

// Reads value from the ADC
void ReadADC(void) {

	uint8_t ui8Index;
	uint16_t adcRX[2];
	uint32_t dummy;

	while (SSIDataGetNonBlocking(SSI2_BASE, &pui32DataRx[0])) {}

	GPIOPinWrite(GPIO_PORTE_BASE, GPIO_PIN_4, 0);

	for (ui8Index = 0; ui8Index < 2; ui8Index++) {

		SSIDataPut(SSI2_BASE, 0x00);

		SSIDataGet(SSI2_BASE, &dummy);

		adcRX[ui8Index] = (dummy);
	}

	GPIOPinWrite(GPIO_PORTE_BASE, GPIO_PIN_4, GPIO_PIN_4);

	convertedValue = (((adcRX[0] << 2) | (adcRX[1] >> 14)) & 0xFFFF);
}

// Function to Write to DAC output
void WriteDAC(uint16_t data) {

	GPIOPinWrite(GPIO_PORTD_BASE, GPIO_PIN_2, 0);

	// Write Command to SPI
	SSIDataPut(SSI3_BASE, 0x08);
	while (SSIBusy(SSI3_BASE)) {
	}
	SSIDataPut(SSI3_BASE, data >> 8);
	while (SSIBusy(SSI3_BASE)) {
	}
	SSIDataPut(SSI3_BASE, data & 0xFF);
	while (SSIBusy(SSI3_BASE)) {
	}

	GPIOPinWrite(GPIO_PORTD_BASE, GPIO_PIN_2, GPIO_PIN_2);

}

